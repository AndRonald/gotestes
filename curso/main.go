package main

import "fmt"

func main()	{	

	// Olá, assim eu faço Hello, World em Go!
	// String
	fmt.Println("Hello, Go Essentials!")
	fmt.Printf("Hello, I'm %s\n", "Hello, World!")

	// Inteiros
	fmt.Printf("Oi, eu sou um inteiro %d\n", 2)
	fmt.Printf("Oi, eu sou um inteiro %d\n", 2)

	//Expressões
	fmt.Printf("I'm %d\n", 2 + 5)

	// Booleanos
	fmt.Printf("I'm %t\n", true)
	fmt.Printf("I'm %t\n",false)
}